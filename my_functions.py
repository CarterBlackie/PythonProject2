def day_of_week(day_number):
    if day_number == 1: 
        return 'Sunday'
    elif day_number == 2: 
        return 'Monday'
    elif day_number == 3: 
        return 'Tuesday'
    elif day_number == 4: 
        return 'Wednesday'
    elif day_number == 5: 
        return 'Thursday'
    elif day_number == 6: 
        return 'Friday'
    elif day_number == 7: 
        return 'Saturday'
    else:
        return 'Error'


